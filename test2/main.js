function natoAlphabet(letter) {
	switch(letter) {
		case "a":
			console.log("alpha");
			break;
		case "b":
			console.log("bravo");
			break;
		case "c":
			console.log("charlie");
			break;
		default:
			console.log("default");
			break;
	}
}

natoAlphabet("c");
natoAlphabet("a");
natoAlphabet("b");
